/**
 * Before production build: set environment.prod.ts apiOrigin from BACKEND_ORIGIN (.env or CI env).
 */
const fs = require('fs');
const path = require('path');
const { loadDotenv } = require('./load-dotenv.cjs');

loadDotenv();

const defaultOrigin = 'http://127.0.0.1:3000';
const apiOrigin = (process.env.BACKEND_ORIGIN || defaultOrigin)
  .trim()
  .replace(/\/$/, '');

const outPath = path.join(__dirname, '..', 'src', 'environments', 'environment.prod.ts');

const content = `/** Generated by scripts/apply-backend-origin.cjs from BACKEND_ORIGIN (.env or env var) */
export const environment = {
  production: true,
  apiOrigin: ${JSON.stringify(apiOrigin)},
  ssrApiOrigin: ${JSON.stringify(apiOrigin)},
};
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log('apply-backend-origin: environment.prod.ts ←', apiOrigin);
